<h3>About</h3>
  <p>Dropbox is great...when you have access to their desktop client. But you can't use the desktop client on public computers in labs, libraries, and internet cafes because you're not given admin privileges to install software.</p>
  <p>Dropbox does have a terrific website that lets you download individual files (or multiple files as a zip archive), but your changes aren't synced until you re-upload the file to the website. Also, Dropbox's website doesn't let you upload entire folders, so it can be a real hassle to work with Dropbox files on a public computer.</p>
  <p>We're building a Dropbox client from scratch that can run from a flash drive. We call it "Lightning Rod."</p>

