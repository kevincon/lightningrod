<h3>FAQ</h3>
<ol>
  <li>
    <p><b>What is Lightning Rod?</b></p>
    <p>Lightning Rod is a Dropbox client for flash drives.</p>
  </li>
  <li>
    <p><b>Wait, doesn't Dropbox already have a client?</b></p>
    <p>If you are using a computer that has restrictions on installing programs,(like in a computer lab, library, or internet cafe), you won't be allowed to install Dropbox's desktop client.</p>
    <p>That's where Lightning Rod comes in. Lightning Rod allows you to change/upload/download your Dropbox files on your flash drive, and any changes you make will be synced automatically.</p>
  </li>
  <li>
    <p><b>Will this work on all computers I take my flash drive with Lightning Rod to?</b></p>
    <p>Yes! Lightning Rod was designed to use Java, which most computers have (Macs even come with it already installed). If you do not have the latest version of Java, you can get it <a href="http://java.com/en/download/index.jsp">here</a>.</p>
  </li>
  <li>
    <p><b>Is there any registration required?</b></p>
    <p>All you need is a Dropbox account.</p>
  </li>
  <li>
    <p><b>How is Lightning Rod different from DropboxPortableAHK?</b><p>
    <p>We believe Lightning Rod is the first cross-platform portable Dropbox client.</p>
    <p>It requires no installation; just drag and drop a file onto your flash drive.</p>
  </li>
